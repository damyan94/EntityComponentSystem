#include "Components/Transform.h"

////////////////////////////////////////////////////////////////////////////////
Transform::Transform()
{
}

////////////////////////////////////////////////////////////////////////////////
Transform::~Transform()
{
}

////////////////////////////////////////////////////////////////////////////////
void Transform::Reset()
{
	m_Position = Position{};
	m_Rotation = Rotation{};
	m_Scale = Scale{};
}

////////////////////////////////////////////////////////////////////////////////
void Transform::SetPosition(Position position)
{
	m_Position = position;
}

////////////////////////////////////////////////////////////////////////////////
void Transform::SetRotation(Rotation rotation)
{
	m_Rotation = rotation;
}

////////////////////////////////////////////////////////////////////////////////
void Transform::SetScale(Scale scale)
{
	m_Scale = scale;
}

////////////////////////////////////////////////////////////////////////////////
Transform::Position Transform::GetPosition() const
{
	return m_Position;
}

////////////////////////////////////////////////////////////////////////////////
Transform::Rotation Transform::GetRotation() const
{
	return m_Rotation;
}

////////////////////////////////////////////////////////////////////////////////
Transform::Scale Transform::GetScale() const
{
	return m_Scale;
}